/**
 * We can have an array of Strings!  This makes it a lot
 * easier to have a collection of names, for example
 */
public class SectionB {
    public static void main(String[] args) {
        String[] students = {"Jay", "Alexus", "Aaron", "Bruce", "Myles", "John"};
        for (int i = 0; i < students.length; i++) {
            System.out.println(students[i]);
        }
    }
}
